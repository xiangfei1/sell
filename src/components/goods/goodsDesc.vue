<template>
  <div class="goodsdesc-container">
    <h3>{{info.title}}</h3>
    <div class="content" v-html="info.content"></div>
  </div>
</template>
<script>
export default {
  data() {
    return {
      info: {}
    };
  },
  created() {
    this.getGoodsDesc();
  },
  methods: {
    getGoodsDesc() {
      this.$http.get("api/goods/getdesc/" + this.$route.params.id).then(rs => {
        if (rs.body.status === 0) {
          this.info = rs.body.message[0];
        }
      });
    }
  }
};
</script>
